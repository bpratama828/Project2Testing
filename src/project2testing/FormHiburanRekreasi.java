    private void cboRekreasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboRekreasiActionPerformed
        String harga = "Rp 0,-", keterangan = null;
        
        harga = txtHarga.getText();
        
            if(cboRekreasi.getSelectedItem() == "Aqua Fun"){
                harga = "35000";
                keterangan = "E-Tiket akan dikirim melalui Email";
            }else if(cboRekreasi.getSelectedItem() == "Dufan"){
                harga = "197000";
                keterangan = "E-Tiket akan dikirim melalui Email";
            }else if(cboRekreasi.getSelectedItem() == "Gondola Ancol"){
                harga = "35000";
                keterangan = "E-Tiket akan dikirim melalui Email";
            }else{
                harga = "0";
            }
        
        txtHarga.setText(harga);
        txtKeterangan.setText(keterangan);
    }//GEN-LAST:event_cboRekreasiActionPerformed

