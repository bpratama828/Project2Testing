    private void btnBeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBeliActionPerformed
        String email, harga;

        String jumlah = txtJumlah.getText();
        harga = txtHarga.getText();
        String total = txtTotal.getText();

        if(jumlah.equals("")){
            JOptionPane.showMessageDialog(null, "Mohon untuk memasukan Jumlah terlebih dahulu");
        }else if(total.equals("") || harga.equals("0")){
            JOptionPane.showMessageDialog(null, "Mohon untuk masukan Total yang benar");
        }else if(harga.equals("") || harga.equals("0")){
            JOptionPane.showMessageDialog(null, "Mohon untuk Pilih Wahana Hiburan terlebih dahulu");
        }else{
            JOptionPane.showMessageDialog(null, "Transaksi Sukses!!");
            this.dispose();
            FormHiburanRekreasi menu = new FormHiburanRekreasi();
            menu.setVisible(true);
        }
    }//GEN-LAST:event_btnBeliActionPerformed

